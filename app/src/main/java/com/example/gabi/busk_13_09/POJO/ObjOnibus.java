public class ObjOnibus {
private String Nome;
private String Linha;
private String Letra;
private String Id;



    public String getNome() {
        return Nome;
    }
    public void setNome(String nome) {
        Nome = nome;
    }
    public String getLinha() {
        return Linha;
    }
    public void setLinha(String linha) {
        Linha = linha;
    }
    public String getLetra() {
        return Letra;
    }
    public void setLetra(String letra) {
        Letra = letra;
    }
    public String getId() {
        return Id;
    }
    public void setId(String id_onibus) {
        this.Id = id_onibus;
    }
}
